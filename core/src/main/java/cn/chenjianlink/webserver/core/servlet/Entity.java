package cn.chenjianlink.webserver.core.servlet;

/*
 * servlet名称对应的类
 */
public class Entity {
    private String name;
    private String clazz;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getClazz() {
        return clazz;
    }

    public void setClazz(String clazz) {
        this.clazz = clazz;
    }
}
